SELECT
  ID,
  NIA_PIN,
  SURNAME,
  MESSAGE,
  CREATE_DATE
FROM
  SIMREG_CORE_TBL_LINKING_REQUESTS
WHERE
  CREATE_DATE LIKE N'%2021-12-22%'
  AND MESSAGE LIKE N '%NIA%'
  AND NIA_PIN IN (
    'GHA-000371263-8',
    'GHA-000373257-9',
    'GHA-000373888-7',
    'GHA-000380566-2',
    'NGA-727424496-5'
  )